/**
 * Get all categories
 */
export function getAllCategories() {
  return [...CATEGORIES];
}

/**
 * Get category by ID
 */
export function getCategoryById(id) {
  return CATEGORIES.find(cat => cat.id === id);
}

/**
 * Search categories by query (name, tags, description)
 */
export function searchCategories(query) {
  if (!query || query.trim() === '') return getAllCategories();
  
  const lowerQuery = query.toLowerCase().trim();
  
  return CATEGORIES.filter(cat => {
    // Search in name
    if (cat.name.toLowerCase().includes(lowerQuery)) return true;
    
    // Search in description
    if (cat.description.toLowerCase().includes(lowerQuery)) return true;
    
    // Search in tags
    if (cat.tags.some(tag => tag.toLowerCase().includes(lowerQuery))) return true;
    
    return false;
  });
}

/**
 * Get categories by tag
 */
export function getCategoriesByTag(tag) {
  const lowerTag = tag.toLowerCase();
  return CATEGORIES.filter(cat => 
    cat.tags.some(t => t.toLowerCase() === lowerTag)
  );
}

/**
 * Get category suggestions (for autocomplete)
 */
export function getCategorySuggestions(query, limit = 5) {
  const results = searchCategories(query);
  return results.slice(0, limit);
}

// =====================================================================
// Firebase Sync (for future use)
// =====================================================================

/**
 * Upload categories to Firestore (run once to populate)
 */
export async function uploadCategoriesToFirestore(db) {
  const { collection, doc, setDoc } = await import('https://www.gstatic.com/firebasejs/11.9.0/firebase-firestore.js');
  
  const categoriesRef = collection(db, 'categories');
  
  for (const category of CATEGORIES) {
    await setDoc(doc(categoriesRef, category.id), {
      ...category,
      createdAt: new Date().toISOString(),
      updatedAt: new Date().toISOString()
    });
  }
  
  console.log('✅ Categories uploaded to Firestore');
}

/**
 * Sync categories from Firestore
 */
export async function syncCategoriesFromFirestore(db) {
  const { collection, getDocs } = await import('https://www.gstatic.com/firebasejs/11.9.0/firebase-firestore.js');
  
  const categoriesRef = collection(db, 'categories');
  const snapshot = await getDocs(categoriesRef);
  
  const firestoreCategories = [];
  snapshot.forEach(doc => {
    firestoreCategories.push({ id: doc.id, ...doc.data() });
  });
  
  return firestoreCategories;
}

